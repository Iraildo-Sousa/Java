    private void botaoUsarRaqueteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoUsarRaqueteMouseClicked
        
        if (mostraLigar.getText().equals("Ligado") && carregar > 0) {
            
            carregar = carregar - 10;
            
            mostraUsar.setText(String.valueOf(carregar) + "%");
            
            mostraTotal();
        
        }
        
        if (mostraLigar.getText().equals("Ligado") && carregar == 0) {
        
            carregar = 0;
            
            mostraTotal();
            JOptionPane.showMessageDialog(null, "Sua bateria est√° descarregada!");
            metodoReset();
        }
        
 
       if (carregar < 100 && carregar > 0 && mostraCarregar.getText().equals("Carregado")) {
        
            mostraCarregar.setText("Descarregando");
                mostraTotal();
        
        }
 
    }//GEN-LAST:event_botaoUsarRaqueteMouseClicked

    private void botaoCarregarRaqueteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoCarregarRaqueteMouseClicked
        
        if (carregar >= 0) {
            
            carregar = 100;

            mostraUsar.setText(String.valueOf(carregar));

            JOptionPane.showMessageDialog(null, "Sua bateria est√° 100% carregada!");
            
            mostraCarregar.setText("Carregado");
            
            mostraTotal();
            
        }
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_botaoCarregarRaqueteMouseClicked


        
