    public void atualizarTabela() {
    
        try {
            
            Connection connection = IntegracaoBanco.conexaoBanco();
            
            String sql = "SELECT * FROM login";
            
            PreparedStatement stmt = connection.prepareStatement(sql);
            
            ResultSet resultSet = stmt.executeQuery();
            
            DefaultTableModel tabelaModelo = (DefaultTableModel) tabela.getModel();
            
            tabelaModelo.setNumRows(0);
            
            while (resultSet.next()) {
            
                Object[] dados = 
                
                {resultSet.getString("id_login"),
                resultSet.getString("usuario"),
                resultSet.getString("situacao")};
                
                tabelaModelo.addRow(dados);
                
            }
            
            resultSet.close();
            stmt.close();
            connection.close();
            
        } catch (SQLException ex) {
          
            Logger.getLogger(TelaEditarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        
        }
    
    }
    
    
