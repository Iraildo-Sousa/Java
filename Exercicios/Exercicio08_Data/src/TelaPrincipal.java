    private void botaoProximoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoProximoMouseClicked
        
        SimpleDateFormat dataFormatada2 = new SimpleDateFormat("dd/MM/yyyy");
        
        String anoCompleto = dataFormatada2.format(calendario.getDate());
        
        SimpleDateFormat dataFormatada = new SimpleDateFormat("yyyy");
        
        String ano = dataFormatada.format(calendario.getDate());
        
        caixaResultado.setText("Ano: " + ano);
        
        int anoNascimento = Integer.parseInt(ano);
        
        int calculo = 2025 - anoNascimento;
        
        DefaultTableModel tabelaModelo = (DefaultTableModel) tabela.getModel();
        
        Object [] dados = {caixaNome.getText(), caixaEmail.getText(), caixaTelefone.getText(), anoCompleto, calculo};
        
        tabelaModelo.addRow(dados);
        
    }//GEN-LAST:event_botaoProximoMouseClicked

